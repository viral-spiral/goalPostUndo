import UIKit

extension UIButton {
    
    
    func setSelectedColour() {
        self.backgroundColor = #colorLiteral(red: 0.2039215686, green: 0.7803921569, blue: 0.3490196078, alpha: 1)
    }//setSelectedColour
    
    
    func setDeselectedColour() {
        self.backgroundColor = #colorLiteral(red: 0.6980392157, green: 0.8666666667, blue: 0.5607843137, alpha: 1)
    }//setDeselectedColour
  
    
}//extension
